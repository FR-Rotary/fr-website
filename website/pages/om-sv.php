<h3>Varför "Rotary"?</h3>

<p>Namnet "Rotary" kommer från den mer kända föreningen Rotary International som en gång i tiden donerade pengar till bygget av studenthemmet. Ifall man letar ordentligt så kan ett och annat Rotary-hjul hittas i närområdet.

<h3>Organisation</h3>

<p>Alla boende på Rotary studenthem är medlemmar i FR Rotary och finansierar verksamheten genom en blygsam månatlig avgift.

<p>Två gånger om året anordnas "husmöten" då alla hyresgäster är välkomna att komma med förslag och söka en plats i styrelsen. Denna styrelse verkar därefter för att realisera de beslut som tas på husmötet genom regelbundna möten inom FR Rotary och även med SGS.

