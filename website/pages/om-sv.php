<h2>Om FR Rotary</h2>

<p>FR Rotary är en ideell förening som verkar för en god social miljö för de boende på Rotary studenthem. Hyresvärden SGS Studentbostäder har drygt 6000 bostäder runtom Göteborg och de stödjer bildandet av så kallade "förtroenderåd" (FR) på de olika bostadsområdena. Dessa FR kommer och går, men FR Rotary är unikt i att ha varit aktivt sedan Rotary studenthems födelse 1967 och har alltid varit starkt och engagerat.

<h3>Hitta hit</h3>

<p>Rotary studenthem är beläget ett stenkast öster om Chalmers runtom vattentornet, som numera också är en del av studenthemmet, högst upp på Johanneberg i Göteborg. Vattentornet syns över hela Göteborg och är ett älskat landmärke för de boende som med dess hjälp alltid kan hitta hem.

<p>Studenthemmet ligger vid slutet av Richertsgatan, en tvärgata till Viktor Rydbergsgatan och studenthemmet Viktor Rydberg ligger som närmsta granne i söder.

<p>Studenthemmets besöksaddress är <em>Richertsgatan 2</em>, men "vid vattentornet i Johanneberg" brukar fungera minst lika bra som vägbeskrivning.

