<h2>Husfester</h2>

<p>Två gånger om året bjuder FR Rotary upp till en traditionellt temabaserad fest som äger rum i Rotary Pub. FR Rotary själva står för allt arbete vad gäller matlagning och servering, med bländande resultat!

<p>Husfesten är subventionerad med syftet att stärka kontakterna mellan de boende. För ett alldeles för lågt pris erbjuds trerätters middag med vin och en lång kväll av kalas.

<p>Festerna äger vanligen rum i april och november. Om du bor på Rotary så missar du inte planscherna som sätts upp—om du inte bor på Rotary så kan du titta på nyheterna på denna sida när det nalkar sig för mer information. Även personer som inte bor på Rotary är välkomna.

