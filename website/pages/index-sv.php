<h3>Löpande verksamhet</h3>

<p>För en överblick av den löpande verksamheten så kan du läsa <a href="<?=$site->link('husmoten')?>">Husmötesprotokoll</a> från de öppna möten som anordnas två gånger om året. Håll ögonen öppna efter kallelserna som anslås på anslagstavlorna runt om på Rotary.

<p>FR Rotary håller en löpande kontakt med hyresvärden <a class="icon external-link" href="http://www.sgs.se/">SGS Studentbostäder</a>. För att få en inblick i samarbetet så kan du blicka över <a href="<?=$site->link('kvartalsmoten')?>">Protokoll från kvartalsmöten</a> mellan FR Rotary och SGS Johanneberg.

<h3>Husfester</h3>

<p>Två gånger om året anordnas det större fester i Rotary Pubs lokaler i FR Rotarys regi. Har du bott här ett tag har du säkert sett planscherna sitta på dörrarna i husfesttider. Om inte, eller om du vill se dem igen liksom bilder från evenemangen så titta in på sidan om våra <a href="<?=$site->link('husfester')?>">Husfester</a>.

