<h2>Bli ledamot i FR Rotary</h2>

<p>Det enda kravet är att du ska vara boende på Rotary studenthem vilket automatiskt gör dig medlem i FR Rotary. Officiellt inval som ledamot i styrelsen sker vanligen vid något av årens husmöten, men vanligast är att du kontaktat styrelsen så fort intresse uppstår, varefter du är med vid våra ordinarie aktiviteter under året och därmed kan bilda dig en klar uppfattning om det är ett engagemang som passar dig.

<p>Förutom de förtroendeposter som finns i styrelsen så kan man bli invald som ledamot, med den lilla betydelseskillnaden att man inte har något stadgeenligt specifikt ansvarsområde.

<p>FR Rotarys huvudsyfte är att verka för en god social miljö på studenthemmet och ett engagemang i styrelsen ska präglas av detsamma. De som går med i styrelsen sitter vanligen kvar tills den dag de lämnar bostadsområdet, men kontakten med de människor man lärt känna slutar inte för det.

