<h2>Ohyran</h2>

<p>Som en indikation på FR Rotarys digra historia så har den tidigare FR-medlemmen Tomten scannat in några nummer utav "Ohyran", den någorlunda fyndigt namngivna kvartalsliga tidskriften som publicerades av FR Rotary för några decennier sedan.

<p>Noterbara notiser är t ex att

<ul>
  <li>man fick 20:-/timme mer i lön för att jobba i puben på den tiden (dvs 20:-/timme)
  <li>SGS gjorde en SGS och höjde hyran med 11.4% 1980, ytterligare 9.77% 1981 och ytterligare 9.1% 1982 till den chockartade nivån 790:-/månad för ett enkelrum
  <li>husfester på den tiden gick med vinst
  <li>Göteborgs kommuns svar på frågan om det bodde någon i det ensliga vattentornet var: "Det går inte att bo där."
  <li>A-huset vid Rotarys invigning innehöll en läkarmottagning för studenter
  <li>utbytet av balkongdörrar påbörjades 1982 och snart är klart.
</ul>

