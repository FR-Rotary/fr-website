<h2>Om FR Rotarys aktiviteter</h2>

<p>En stor del av FR Rotarys löpande uppgifter innefattar kontakt med SGS angående hyresgästers önskemål angående områdets fortsatta utveckling. I egenskap av hyresgäster är vi även en länk som SGS använder för att komma i kontakt med de boende.

<p>Våra aktiviteter skiftar löpande, men större projekt som länge hållits igång innefattar:

<dl>
  <dt>Rotary Pub
  <dd>I studenthemmet drivs pubverksamhet med allmänt tillstånd. Verksamheten sköts ideellt genom "Källarmästeriet", en underorganisation inom FR Rotary. För mer information, se <a class="icon external-link" href="http://www.rotarypub.se/">Rotary Pubs hemsida</a>.

  <dt>Husfester
  <dd>Två gånger om året organiseras en större fest i Rotary Pubs lokaler i FR Rotarys regi. Kostnaderna subventioneras av FR Rotary och för ett lågt pris erbjuds en lång kväll med trerätters middag och ett glatt FR Rotary. För mer information om gångna husfester, se <a href="<?=$site->link('husfester')?>">Husfester</a>.

  <dt>Grillfest
  <dd>Innan skolornas sommaruppehåll anordnar FR Rotary en stor grillfest på gården där det säljs mat till ett subventionerat pris. Inte minst då alla studenter uppskattar billig mat så brukar det vara stor uppslutning.

  <dt>Gymnastiksal
  <dd>I studenthemmet finns tillgång till en gymnastiksal som är gratis att boka för de boende. FR Rotary står även för utlåning av materiel till diverse sportaktiviteter.

  <dt>Hobbyrum och verktyg
  <dd>Om du behöver en oöm lokal för ett mindre projekt så finns ett hobbyrum som hyresgäster kan boka. FR Rotary står även för utlåning av en mängd olika verktyg såsom symaskin, skruvdragare, enklare lödutrustning, etc. Kontakta hustomte at fr-rotary com vid intresse.

  <dt>Utlåning av spel
  <dd>I Rotary Pub har FR Rotary placerat ett gäng sällskapsspel som kan lånas av hyresgäster. Kontakta puben under dess öppettider vid intresse.

  <dt>Biljard
  <dd>FR Rotary har införskaffat ett biljardbord med tillhörande utrustning för användning av hyresgäster. Bokning sker via "Mina sidor" på SGS hemsida eller de terminaler som är utplacerade vid tvättstugorna. Bollar och köer finns på plats i rummet.
</dl>

<p>Detta var ett axplock av FR Rotarys projekt, mer info finns att beskåda på det <a class="icon document" href="documents/fr-rotary-information-2011-09-12.pdf">informationsblad</a> du som hyresgäst antagligen redan erhållit i samband med inflytt.
