<h2>About FR Rotary</h2>

<p>FR Rotary is a non-profit organization with the task of contributing to a good social environment for the tenants at Rotary student housing. The landlord SGS Studentbostäder has more than 6000 rooms in Gothenburg and they support the creation of so called "förtroenderåd" (FR) in the different areas. These FR come and go, but FR Rotary is unique in that it has been running since the birth of Rotary student housing in 1967, always strong and active.

<h3>Find us</h3>

<p>Rotary student housing is situated a stone's throw east of Chalmers around the water tower, which nowadays also is a part of the student housing, on top of Johanneberg in Göteborg. The water tower is visible all over Gothenburg and is a beloved land mark for the here living that with its help always can find their way home.

<p>One finds the housing at the end of Richertsgatan, a cross street to Viktor Rydbergsgatan and the student housing Viktor Rydberg is its closest neighbour to the south.

<p>Rotary's visiting address is <em>Richertsgatan 2</em>, but "by the water tower in Johanneberg" usually works just as well as directions.

