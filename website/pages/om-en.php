<h3>Why "Rotary"?</h3>

<p>The name "Rotary" is derived from the more famous organization Rotary International that donated funds for building of the student housing. If one searches the area thoroughly, one might find some Rotary International emblems.

<h3>Organization</h3>

<p>Everyone living at Rotary student housing is a part of FR Rotary and finance the activities through a modest monthly fee.

<p>Twice a year "House meetings" are arranged where every tenant is welcome to bring up suggestions and apply for the board. This board will thereafter work to realize the decisions that were taken during the house meeting through regular board meetings within FR Rotary and with SGS.

